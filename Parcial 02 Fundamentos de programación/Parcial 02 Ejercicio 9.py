##Ejercicio 9
##1. Toma la cadena "Any time y Anytime".
##2. Reemplaza todas las apariciones de "Any time" (con espacio) por "Always".
##3. Convierte todo el texto resultante a mayusculas.


## Tomar la cadena "Any time y Anytime"
texto = "Any time y Anytime"
print(texto)

## Reemplazar "Any time" (con espacio) por "Always"
texto_reemplazado = texto.replace("Any time", "Always")
print(texto_reemplazado)

## Convertir todo el texto a mayúsculas
texto_mayusculas = texto_reemplazado.upper()
print(texto_mayusculas)
